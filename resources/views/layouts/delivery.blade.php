<h3>Kurirska isporuka na teritoriji Srbije</h3><br>
<h4>Rok Isporuke</h4><br><br>

<p>Nakon poručivanja, bićete kontaktirani u vezi Vaše porudžbine putem telefona. Period isporuke porudžbine je 2-3 radna dana. Porudžbine poslate petkom i
    vikendom biće dostavljene naredne nedelje.</p><br><br>

<h4>Preuzimanje pošiljke</h4><br><br>

<p>Pošiljka stiže na adresu u periodu od 8-16h. Potrebno je da u tom periodu na adresi bude osoba koja može preuzeti pošiljku.
Kurir svaku pošiljku pokušava da uruči u dva navrata. Ukoliko Vas kurir ne pronađe na adresi, pozvaće vas na telefon koji ste ostavili
prilikom kreiranja porudžbenice i dogovoriti novi termin isporuke. Ukoliko Vas ni drugog puta ne pronađe na adresi, pošiljka se vraća u
    prostorije Đak Sporta.</p><br><br>

<h4>Cena isporuke</h4><br><br>

<p>Cena isporuke za sve porudžbine je besplatna. U slučaju zamene, troškove vraćanja robe snosi kupac.</p><br><br>
